#include<bits/stdc++.h>
using namespace std;
int main(){
    string S;
    cin>>S;                       //The cin object in C++ is an object of class iostream. It is used to accept the input from the standard input device i.e. keyboard
    cout<<"Hello, "<<S<<endl;    /*The cout object in C++ is an object of class iostream.
                                  It is defined in iostream header file. It is used to display the output to the standard output device i.e. monitor*/
    return 0;
}
